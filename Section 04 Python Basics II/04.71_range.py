# Range()
for number in range(0, 10):
    print('email email list')

# You can use _ if you don't need a variable
for _ in range(0, 10):
    print(_)

# You can use a 3rd parameter that is the step
for _ in range(0, 10, 2):
    print(_)

# you can step back as well, you must put the 1st and 2nd parameter is reverse order
for _ in range(10, 0, -1):
    print(_)

for _ in range(2):
    print(list(range(10)))
