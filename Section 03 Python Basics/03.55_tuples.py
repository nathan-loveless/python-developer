# Tuple
# Like lists except immutable, not sortable, not reversible
# like dictionaries you can also see if a value is in a tuple
# Why use a tuple instead of list?  It is immutable, so it tells other programmers, hey this is not to be changed at all
# and safer
# it is less flexible because you can't sort or reverse it, but it is a little faster

my_tuple = (1, 2, 3, 4, 5)
print(5 in my_tuple)
