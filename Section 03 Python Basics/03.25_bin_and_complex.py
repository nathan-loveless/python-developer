# A complex number is a number that is a 3rd type instead of int and float it is a real number
# 5 gets stored in binary in memory
# You can look this up, it is not used 99.9% of the time
# complex()

# Binary will return the binary number of the number in parentheses
# the b signifies a binary number
print(bin(5))

# you can convert it back to an int
# the 2nd argument is the base of the number, for this it is base 2 because it is binary
print(int('0b101', 2))
